void SELF::serialize(std::ostream & ostr) const
{
    ostr << '{'
         << "line1=" << get_line1()  << ","
         << "line2=" << get_line2()  
         << '}';
}

void SELF::serializeWithPrecision(std::ostream & ostr) const
{
    ostr << '{';
    SPL::serializeWithPrecision(ostr << "line1=", get_line1()) << ",";
    SPL::serializeWithPrecision(ostr << "line2=", get_line2()) ;
    ostr << '}';
}

